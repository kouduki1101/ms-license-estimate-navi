import fs from "node:fs/promises";
import path from "node:path";

const SNAPSHOT_HEADERS = [
  "snapshot_id",
  "sku_id",
  "captured_at",
  "price_usd",
  "fx_rate_usd_jpy",
  "price_jpy",
  "source_type",
  "source_url",
  "confidence",
  "effective_start_date",
  "change_note",
];

const DEFAULT_SOURCE_URL = "https://prices.azure.com/api/retail/prices";

function usage() {
  return [
    "Usage:",
    "  node .\\scripts\\append-azure-price-snapshot.mjs --azure-csv <csv> --sku-id <sku> --row <n> --fx-rate <rate> [options]",
    "",
    "Options:",
    "  --azure-csv <csv>           CSV generated by fetch-azure-retail-prices.mjs",
    "  --sku-id <sku>              Target sku_id in sku_price_snapshot.csv",
    "  --row <n>                   1-based data row number from azure CSV, excluding header",
    "  --snapshot-csv <csv>        Defaults to sku_price_snapshot.csv",
    "  --fx-rate <rate>            Required for JPY conversion",
    "  --captured-at <datetime>    Defaults to current UTC ISO timestamp",
    "  --snapshot-id <id>          Defaults to AZURE-SNAP-{timestamp}-{sku}",
    "  --source-url <url>          Defaults to Azure Retail Prices API URL",
    "  --confidence <value>        Defaults to High",
    "  --note <text>               Optional change_note",
    "  --dry-run                   Print row but do not write",
    "",
    "Row selection helpers:",
    "  Use --row after reviewing the candidate CSV. This keeps the import explicit.",
  ].join("\n");
}

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const key = argv[i];
    if (!key.startsWith("--")) throw new Error(`Unexpected positional argument: ${key}`);
    const next = argv[i + 1];
    if (!next || next.startsWith("--")) {
      args[key.slice(2)] = true;
    } else {
      args[key.slice(2)] = next;
      i += 1;
    }
  }
  return args;
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;
  const input = text.replace(/^\uFEFF/, "");

  for (let i = 0; i < input.length; i += 1) {
    const char = input[i];
    const next = input[i + 1];
    if (char === '"' && inQuotes && next === '"') {
      cell += '"';
      i += 1;
    } else if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") i += 1;
      row.push(cell);
      if (row.some((value) => value.length > 0)) rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }

  row.push(cell);
  if (row.some((value) => value.length > 0)) rows.push(row);
  if (rows.length === 0) return [];

  const headers = rows[0].map((header) => header.trim());
  return rows.slice(1).map((values, index) => ({
    __rowNumber: index + 1,
    ...Object.fromEntries(headers.map((header, valueIndex) => [header, values[valueIndex] ?? ""])),
  }));
}

function csvCell(value) {
  const text = value === null || value === undefined ? "" : String(value);
  return /[",\r\n]/.test(text) ? `"${text.replace(/"/g, '""')}"` : text;
}

function rowToCsv(row) {
  return SNAPSHOT_HEADERS.map((header) => csvCell(row[header])).join(",");
}

function numberValue(value, field) {
  const parsed = Number(String(value ?? "").replace(/,/g, ""));
  if (!Number.isFinite(parsed)) throw new Error(`Invalid numeric ${field}: ${value}`);
  return parsed;
}

function compactIdPart(value) {
  return String(value || "")
    .toUpperCase()
    .replace(/[^A-Z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 32);
}

function defaultSnapshotId(skuId, capturedAt, azureRow) {
  const datePart = capturedAt.replace(/[^0-9]/g, "").slice(0, 14);
  const meterPart = compactIdPart(azureRow.meterName || azureRow.skuName || azureRow.productName || "METER");
  return `AZURE-SNAP-${datePart}-${compactIdPart(skuId)}-${meterPart}`;
}

function buildSnapshotRow(args, azureRow) {
  const capturedAt = args["captured-at"] || new Date().toISOString();
  const priceUsd = numberValue(azureRow.retailPrice || azureRow.unitPrice, "retailPrice/unitPrice");
  const fxRate = numberValue(args["fx-rate"], "fx-rate");
  const priceJpy = priceUsd * fxRate;
  const meterSummary = [
    azureRow.serviceName,
    azureRow.productName,
    azureRow.skuName,
    azureRow.meterName,
    azureRow.armRegionName,
    azureRow.unitOfMeasure,
  ].filter(Boolean).join(" / ");

  return {
    snapshot_id: args["snapshot-id"] || defaultSnapshotId(args["sku-id"], capturedAt, azureRow),
    sku_id: args["sku-id"],
    captured_at: capturedAt,
    price_usd: priceUsd.toFixed(6).replace(/\.?0+$/, ""),
    fx_rate_usd_jpy: fxRate.toString(),
    price_jpy: priceJpy.toFixed(6).replace(/\.?0+$/, ""),
    source_type: "azure_retail_api",
    source_url: args["source-url"] || DEFAULT_SOURCE_URL,
    confidence: args.confidence || "High",
    effective_start_date: azureRow.effectiveStartDate || "",
    change_note: args.note || `Azure Retail Prices API candidate: ${meterSummary}`,
  };
}

async function appendSnapshot(snapshotCsv, row) {
  let existing = "";
  try {
    existing = await fs.readFile(snapshotCsv, "utf8");
  } catch (error) {
    if (error.code !== "ENOENT") throw error;
  }

  const needsHeader = existing.trim().length === 0;
  const prefix = needsHeader ? `${SNAPSHOT_HEADERS.join(",")}\n` : existing.endsWith("\n") ? "" : "\n";
  await fs.mkdir(path.dirname(snapshotCsv), { recursive: true });
  await fs.appendFile(snapshotCsv, `${prefix}${rowToCsv(row)}\n`, "utf8");
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help || args.h) {
    console.log(usage());
    return;
  }
  for (const required of ["azure-csv", "sku-id", "row", "fx-rate"]) {
    if (!args[required]) throw new Error(`Missing required --${required}.\n\n${usage()}`);
  }

  const azureCsv = path.resolve(args["azure-csv"]);
  const snapshotCsv = path.resolve(args["snapshot-csv"] || "sku_price_snapshot.csv");
  const azureRows = parseCsv(await fs.readFile(azureCsv, "utf8"));
  const rowNumber = Number.parseInt(args.row, 10);
  const selected = azureRows.find((row) => row.__rowNumber === rowNumber);
  if (!selected) {
    throw new Error(`Row ${args.row} not found in ${azureCsv}. Data rows available: ${azureRows.length}`);
  }

  const snapshotRow = buildSnapshotRow(args, selected);
  if (!args["dry-run"]) {
    await appendSnapshot(snapshotCsv, snapshotRow);
  }

  console.log(JSON.stringify({
    dryRun: Boolean(args["dry-run"]),
    snapshotCsv,
    selectedAzureRow: rowNumber,
    snapshotRow,
  }, null, 2));
}

main().catch((error) => {
  console.error(error.message || String(error));
  process.exit(1);
});
